// Simple arithmetic operations (addition, subtraction)
#include <iostream>
using namespace std;

int main() {
    int num1 = 10, num2 = 5;
    int addition = num1 + num2;
    int subtraction = num1 - num2;
    cout << "Addition: " << num1 << " + " << num2 << " = " << addition << endl;
    cout << "Subtraction: " << num1 << " - " << num2 << " = " << subtraction << endl;
    return 0;
}
