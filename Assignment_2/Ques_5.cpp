// Store and display different data types
#include <iostream>
using namespace std;

int main() {
    char ch = 'A';
    double d = 5.6;
    bool b = true;
    int i = 10;
    cout << "Character: " << ch << endl;
    cout << "Double: " << d << endl;
    cout << "Boolean: " << (b ? "true" : "false") << endl;
    cout << "Integer: " << i << endl;
    return 0;
}
